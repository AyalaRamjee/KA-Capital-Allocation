// Utility functions
export const formatCurrency = (amount: number): string => {
  if (amount >= 1000000000) return `$${(amount / 1000000000).toFixed(1)}B`;
  if (amount >= 1000000) return `$${(amount / 1000000).toFixed(1)}M`;
  if (amount >= 1000) return `$${(amount / 1000).toFixed(1)}K`;
  return `$${amount.toFixed(0)}`;
};

export const calculateNPV = (cashFlows: { year: number; amount: number }[], discountRate: number = 10): number => {
  return cashFlows.reduce((npv, cf) => {
    return npv + cf.amount / Math.pow(1 + discountRate / 100, cf.year);
  }, 0);
};

export const calculateIRR = (cashFlows: { year: number; amount: number }[]): number => {
  let rate = 0.1;
  for (let i = 0; i < 100; i++) {
    const npv = cashFlows.reduce((sum, cf) => sum + cf.amount / Math.pow(1 + rate, cf.year), 0);
    if (Math.abs(npv) < 1000) break;
    rate += npv > 0 ? 0.001 : -0.001;
  }
  return rate * 100;
};

export const calculateMIRR = (cashFlows: { year: number; amount: number }[], financeRate: number = 10, reinvestRate: number = 12): number => {
  const positiveFlows = cashFlows.filter(cf => cf.amount > 0);
  const negativeFlows = cashFlows.filter(cf => cf.amount < 0);
  
  const fvPositive = positiveFlows.reduce((sum, cf) => {
    const yearsToEnd = Math.max(...cashFlows.map(c => c.year)) - cf.year;
    return sum + cf.amount * Math.pow(1 + reinvestRate / 100, yearsToEnd);
  }, 0);
  
  const pvNegative = negativeFlows.reduce((sum, cf) => {
    return sum + cf.amount / Math.pow(1 + financeRate / 100, cf.year);
  }, 0);
  
  const n = Math.max(...cashFlows.map(c => c.year));
  return (Math.pow(fvPositive / Math.abs(pvNegative), 1 / n) - 1) * 100;
};

// Portfolio Analysis Functions
export const calculatePortfolioMetrics = (projects: any[]) => {
  if (projects.length === 0) {
    return {
      projectCount: 0,
      totalCapex: 0,
      totalNPV: 0,
      portfolioIRR: 0,
      avgPayback: 0,
      avgRisk: 0
    };
  }
  
  const totalCapex = projects.reduce((sum, p) => sum + p.capex, 0);
  const totalNPV = projects.reduce((sum, p) => sum + p.npv, 0);
  const weightedIRR = projects.reduce((sum, p) => sum + (p.irr * p.capex), 0) / totalCapex;
  const avgPayback = projects.reduce((sum, p) => sum + (p.paybackYears * p.capex), 0) / totalCapex;
  const avgRisk = projects.reduce((sum, p) => sum + p.riskScore, 0) / projects.length;
  
  return {
    projectCount: projects.length,
    totalCapex,
    totalNPV,
    portfolioIRR: weightedIRR,
    avgPayback: Number(avgPayback.toFixed(1)),
    avgRisk: Number(avgRisk.toFixed(1))
  };
};

export const autoBalanceBudgets = (domains: any[], changedDomainId: string, newValue: number) => {
  const changedDomain = domains.find(d => d.id === changedDomainId);
  const otherDomains = domains.filter(d => d.id !== changedDomainId && d.isActive);
  
  if (!changedDomain || otherDomains.length === 0) return domains;
  
  const totalOther = 100 - newValue;
  const equalShare = totalOther / otherDomains.length;
  const totalBudget = 1000000000;
  
  return domains.map(domain => {
    if (domain.id === changedDomainId) {
      return {
        ...domain,
        budgetPercent: newValue,
        budget: (totalBudget * newValue) / 100,
        remainingBudget: (totalBudget * newValue) / 100
      };
    } else if (domain.isActive) {
      return {
        ...domain,
        budgetPercent: equalShare,
        budget: (totalBudget * equalShare) / 100,
        remainingBudget: (totalBudget * equalShare) / 100
      };
    }
    return domain;
  });
};

export const filterProjectsByRisk = (projects: any[], riskTolerance: string) => {
  const riskMap = {
    'low': projects.filter(p => p.riskScore <= 3),
    'medium': projects.filter(p => p.riskScore <= 6),
    'high': projects // all projects
  };
  return riskMap[riskTolerance as keyof typeof riskMap] || projects;
};

export const allocationPatterns = {
  evenSpread: (total: number, quarters: number) => {
    const perQuarter = total / quarters;
    return Array(quarters).fill(perQuarter);
  },
  frontLoaded: (total: number, quarters: number) => {
    const weights = [0.4, 0.3, 0.2, 0.1];
    return weights.slice(0, quarters).map(w => total * w);
  },
  backLoaded: (total: number, quarters: number) => {
    const weights = [0.1, 0.2, 0.3, 0.4];
    return weights.slice(0, quarters).map(w => total * w);
  },
  sCurve: (total: number, quarters: number) => {
    const points = [];
    for (let i = 0; i < quarters; i++) {
      const t = i / (quarters - 1);
      const sCurveValue = 1 / (1 + Math.exp(-12 * (t - 0.5)));
      points.push(sCurveValue);
    }
    const sum = points.reduce((a, b) => a + b, 0);
    return points.map(p => (p / sum) * total);
  }
};

